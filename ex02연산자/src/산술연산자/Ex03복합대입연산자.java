package 산술연산자;

public class Ex03복합대입연산자 {

	public static void main(String[] args) {
		// = 대입 연산자
		// 연산의 결과를 담을때 사용!
		// +을 기준으로 오른쪽의 데이터를 왼쪽에 담아준다
		// 복합 대입 연산자
		// : 변수에 값을 할당하면서 동시에 산술연산
		// += -+ *= 
		int num1 = 10;
		num1-=2; //num1=num1-2;
		// num1에서 2를 뺀 결과 값을 num1에 할당
		System.out.println(num1); //8
		num1+=2;
		System.out.println(num1);
		
		
		
		
		
		
		
	}

}
